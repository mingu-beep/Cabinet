package com.cabinet.Cabinet.dto;

public class CategoryDTO {
    private int ctNo;
    private String ctType;
    private int ctCNT;

    public int getCtNo() {
        return ctNo;
    }

    public void setCtNo(int ctNo) {
        this.ctNo = ctNo;
    }

    public String getCtType() {
        return ctType;
    }

    public void setCtType(String ctType) {
        this.ctType = ctType;
    }

    public int getCtCNT() {
        return ctCNT;
    }

    public void setCtCNT(int ctCNT) {
        this.ctCNT = ctCNT;
    }
}
